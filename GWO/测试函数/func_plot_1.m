% 画图程序，用于绘制不同测试函数的三维曲面图
function func_plot_1(func_name)
    % 调用 Get_Functions_details_1 函数，获取指定测试函数的相关信息
    % lb 为变量的下限，ub 为变量的上限，dim 为变量的维度，fobj 为目标函数句柄
    [lb,ub,dim,fobj]=Get_Functions_details_1(func_name);

    % 根据不同的测试函数名，确定绘图时 x 和 y 轴的取值范围
    switch func_name 
        case 'F1' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2
            x=-100:2:100; y=x; %[-100,100]
        case 'F2' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2，注释中区间 [-10,10] 可能有误
            x=-100:2:100; y=x; %[-10,10]
        case 'F3' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2
            x=-100:2:100; y=x; %[-100,100]
        case 'F4' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2
            x=-100:2:100; y=x; %[-100,100]
        case 'F5' 
            % x 和 y 的取值范围为 -200 到 200，步长为 2，注释中区间 [-5,5] 可能有误
            x=-200:2:200; y=x; %[-5,5]
        case 'F6' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2
            x=-100:2:100; y=x; %[-100,100]
        case 'F7' 
            % x 和 y 的取值范围为 -1 到 1，步长为 0.03
            x=-1:0.03:1;  y=x;  %[-1,1]
        case 'F8' 
            % x 和 y 的取值范围为 -500 到 500，步长为 10
            x=-500:10:500;y=x; %[-500,500]
        case 'F9' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5;   y=x; %[-5,5]    
        case 'F10' 
            % x 和 y 的取值范围为 -20 到 20，步长为 0.5，注释中区间 [-500,500] 可能有误
            x=-20:0.5:20; y=x;%[-500,500]
        case 'F11' 
            % x 和 y 的取值范围为 -500 到 500，步长为 10，注释中区间 [-0.5,0.5] 可能有误
            x=-500:10:500; y=x;%[-0.5,0.5]
        case 'F12' 
            % x 和 y 的取值范围为 -10 到 10，步长为 0.1，注释中区间 [-pi,pi] 可能有误
            x=-10:0.1:10; y=x;%[-pi,pi]
        case 'F13' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.08，注释中区间 [-3,1] 可能有误
            x=-5:0.08:5; y=x;%[-3,1]
        case 'F14' 
            % x 和 y 的取值范围为 -100 到 100，步长为 2
            x=-100:2:100; y=x;%[-100,100]
        case 'F15' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]
        case 'F16' 
            % x 和 y 的取值范围为 -1 到 1，步长为 0.01，注释中区间 [-5,5] 可能有误
            x=-1:0.01:1; y=x;%[-5,5]
        case 'F17' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]
        case 'F18' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.06
            x=-5:0.06:5; y=x;%[-5,5]
        case 'F19' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]
        case 'F20' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]        
        case 'F21' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]
        case 'F22' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]     
        case 'F23' 
            % x 和 y 的取值范围为 -5 到 5，步长为 0.1
            x=-5:0.1:5; y=x;%[-5,5]  
    end    

    % 获取 x 向量的长度，用于后续的双重循环
    L=length(x);
    % 初始化一个空矩阵 f，用于存储目标函数在不同 (x, y) 点的取值
    f=[];

    % 双重循环遍历 x 和 y 向量的所有组合
    for i=1:L
        for j=1:L
            % 如果当前测试函数不是 F15、F19、F20、F21、F22、F23
            if strcmp(func_name,'F15')==0 && strcmp(func_name,'F19')==0 && strcmp(func_name,'F20')==0 && strcmp(func_name,'F21')==0 && strcmp(func_name,'F22')==0 && strcmp(func_name,'F23')==0
                % 计算目标函数在 (x(i), y(j)) 点的取值，并存储在 f(i, j) 中
                f(i,j)=fobj([x(i),y(j)]);
            end
            % 如果当前测试函数是 F15
            if strcmp(func_name,'F15')==1
                % 计算目标函数在 (x(i), y(j), 0, 0) 点的取值，并存储在 f(i, j) 中
                f(i,j)=fobj([x(i),y(j),0,0]);
            end
            % 如果当前测试函数是 F19
            if strcmp(func_name,'F19')==1
                % 计算目标函数在 (x(i), y(j), 0) 点的取值，并存储在 f(i, j) 中
                f(i,j)=fobj([x(i),y(j),0]);
            end
            % 如果当前测试函数是 F20
            if strcmp(func_name,'F20')==1
                % 计算目标函数在 (x(i), y(j), 0, 0, 0, 0) 点的取值，并存储在 f(i, j) 中
                f(i,j)=fobj([x(i),y(j),0,0,0,0]);
            end       
            % 如果当前测试函数是 F21、F22 或 F23
            if strcmp(func_name,'F21')==1 || strcmp(func_name,'F22')==1 ||strcmp(func_name,'F23')==1
                % 计算目标函数在 (x(i), y(j), 0, 0) 点的取值，并存储在 f(i, j) 中
                f(i,j)=fobj([x(i),y(j),0,0]);
            end          
        end
    end

    % 绘制三维曲面图，使用 surfc 函数，设置线条样式为无
    surfc(x,y,f,'LineStyle','none');

end